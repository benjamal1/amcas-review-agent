#!/usr/bin/env node
// Generates AGENTS.md (Codex fallback) = root CLAUDE.md router + all .claude/agents/*.md bodies
// inlined, since Codex has no subagent dispatch. Run from repo root: `node Agent/build-agents-md.mjs`
import { readFileSync, writeFileSync, readdirSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const router = readFileSync(path.join(repoRoot, 'CLAUDE.md'), 'utf8')
const agentsDir = path.join(repoRoot, '.claude', 'agents')
const agentFiles = readdirSync(agentsDir).filter(f => f.endsWith('.md')).sort()

const stripFrontmatter = (s) => s.replace(/^---\n[\s\S]*?\n---\n/, '')

let out = `<!-- GENERATED by Agent/build-agents-md.mjs — edit CLAUDE.md + .claude/agents/, then rerun. -->
<!-- Codex reads this file (no subagent dispatch); the router's dispatch table is advisory here —
     the relevant subagent body is inlined below. -->

${router}

---

# Subagents (inlined for Codex)

`
for (const f of agentFiles) {
  const body = stripFrontmatter(readFileSync(path.join(agentsDir, f), 'utf8')).trim()
  out += `\n## ${f.replace(/\.md$/, '')}\n\n${body}\n\n---\n`
}

writeFileSync(path.join(repoRoot, 'AGENTS.md'), out)
console.log(`AGENTS.md written: router + ${agentFiles.length} subagents (${agentFiles.map(f=>f.replace(/\.md$/,'')).join(', ')})`)
